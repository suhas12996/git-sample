package DataTypes;

public class IfElseConcept {
	
	public static void main(String[] args) {
		//If loop Concept
		
		int c=100;
		int d=300;
		
		if(c>=d) {
			System.out.println(c);
		}
			else
				System.out.println(d);
		
		//Nested Loop concept
		
		int a=10; 
		int b=20;
		
		if (a==b)
		{
			System.out.println("both values are equal");
		}
			else
		if (b!=a)
		{
			System.out.println("both values are not equal");
		}
	}
		
}


